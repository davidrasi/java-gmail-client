    public static void generateAndSendEmail() throws AddressException, MessagingException {

//Paso 1
		System.out.println("\n1 ===> Configurando las propiedades del servidor de correo..");
		mailServerProperties = System.getProperties();
		mailServerProperties.put("mail.smtp.port", "587");
		mailServerProperties.put("mail.smtp.auth", "true");
		mailServerProperties.put("mail.smtp.starttls.enable", "true");
		System.out.println("Configuracion correcta..");

//Paso 2
		System.out.println("\n\n2 ===> Estableciendo Conexi√≥n..");
		getMailSession = Session.getDefaultInstance(mailServerProperties, null);
		generateMailMessage = new MimeMessage(getMailSession);
		generateMailMessage.addRecipient(Message.RecipientType.TO, new InternetAddress(para));
		generateMailMessage.addRecipient(Message.RecipientType.CC, new InternetAddress(de));
		generateMailMessage.setSubject(asunto);
		String emailBody = texto;
		generateMailMessage.setContent(emailBody, "text/html");
		System.out.println("La sesi√≥n se ha establecido con exito..");

//Paso 3
		System.out.println("\n\n3 ===> Enviamdo mail");
		Transport transport = getMailSession.getTransport("smtp");
                
		transport.connect("smtp.gmail.com", de, pass);
                
		transport.sendMessage(generateMailMessage, generateMailMessage.getAllRecipients());
		transport.close();
	}




